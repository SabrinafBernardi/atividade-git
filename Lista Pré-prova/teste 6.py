nums = [1, 2, 3] #Cria uma lista com três valores
total = 0  #Cria uma variável para adicionar a soma
for n in nums: #Cria um laço de repetição, enquanto houver números na lista ele vai rodar
    total += n #Sempre irá adicionar o valor da variável somado com o valor da lista
print(total) #Mostra na tela o valor final do contador