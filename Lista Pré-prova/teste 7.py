nums = [1, 2, 3, 4] #Cria uma lista com quatro valores
soma = 0 #Cria uma variável para adicionar a soma
for i in range(len(nums)): #Cria um laço de repetição com os valores do indice da vez
    soma += nums[i] #Soma "soma" com os valores do indice da lista
print(soma) #Mostra na tela o valor da soma